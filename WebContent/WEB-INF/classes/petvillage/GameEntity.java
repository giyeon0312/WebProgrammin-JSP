package petvillage;

public class GameEntity {
	private String user_id;
	private int brick_out;
	private int dino_jump;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getBrick_out() {
		return brick_out;
	}
	public void setBrick_out(int brick_out) {
		this.brick_out = brick_out;
	}
	public int getDino_jump() {
		return dino_jump;
	}
	public void setDino_jump(int dino_jump) {
		this.dino_jump = dino_jump;
	}
}

